
public class Livro {
    
    String Título;
    int Páginas;
    String Autor;
    String Editora;
    double Preço;
    
    void imprimir(){
        System.out.println("Título "+this.Título);
        System.out.println("Páginas "+this.Páginas);
        System.out.println("Autor "+this.Autor);
        System.out.println("Editora "+this.Editora);
        System.out.println("Preço "+this.Preço);
    }
}
